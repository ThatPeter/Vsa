package cvicenie4_01c;

import java.io.Serializable;
import javax.persistence.CascadeType;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToOne;

/**
 *
 * @author illuSion
 */
@Entity
public class Kniha implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;
    private String nazov;
    //s anotaciou vyzera tak isto ako bez - stlpec vydavatel ID v tabulke kniha
    @ManyToOne(cascade=CascadeType.PERSIST) //bez anotacky to vygenilo tabulku knihy so stlpcov vydavatel id
    private Vydavatelstvo vydavatel;
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNazov() {
        return nazov;
    }

    public void setNazov(String nazov) {
        this.nazov = nazov;
    }

    public Vydavatelstvo getVydavatel() {
        return vydavatel;
    }

    public void setVydavatel(Vydavatelstvo vydavatel) {
        this.vydavatel = vydavatel;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Kniha)) {
            return false;
        }
        Kniha other = (Kniha) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "cvicenie4_01c.Kniha[ id=" + id + " ]";
    }
    
}
